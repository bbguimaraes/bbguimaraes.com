Known and frequented since antiquity, it appears in the writings of Pliny the
Elder (who died attempting to rescue victims of the eruption of <a
href="vesuvius.html">Vesuvius</a>).  It is thought to have been part of the
Pentapoli Aurunca, a group of cities which predate the Roman conquest of the
region, as the “Aquae Vescinae” of the ancient city of Vescia.  The Greek
philosopher Plotinus was treated there and is thought to have died and be buried
in the church of Santa Maria in Pensulis.
]],
